# Print out the last 3 letters
letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]
# Solution
print(letters[:])
print(1, "\t", letters[::-1])
print(2, "\t", letters[::-2])
print(3, "\t", letters[1:7])
print(4, "\t", letters[1:7:-2])
print(5, "\t", letters[-5:-1:2])
print(55, "\t", letters[-5:-1:-2])
print(6, "\t", letters[-15:1])
print(7, "\t", letters[-15:0])
print(8, "\t", letters[-15:-1])
print(9, "\t", letters[-15:15])
print(10, "\t", letters[-4: -6])
print(11, "\t", letters[-5: -2])

print(15, "\t", letters[-3:])
# When you don't put any index to the right of the colon everything is included and upper-bound exclusivity is ignored.

