# Exercise No.64

# How would you make the following code print "Hello" and "Hi" repeatedly by just changing the line with the "break"
while True:
	print("Hello")
	if 2 > 1:
		break
	print("Hi")


# Solution
# You can switch "break" to "pass" or to some random operation like 1 + 2 or 4 * 1
# The pass statement does nothing particular, but it can act as a placeholder For example ,when creating a class and
# one of its methods is not defined yet, if you run the program the compiler will expect indented statements below that
# method but if it's empty (the method) then an error occurs. You can place a pass below that method while you continue
# finishing your class.


